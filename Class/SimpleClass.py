# Example: Simple Python Class
# ----------------------------------------------

# A class is a blueprint (template) used to create objects.
# Classes are used to group related data (variables) and functions (methods) together.
# This makes the code more organized, reusable, and modular.

class PythonSimple:
    # The __init__ method is a special method (called automatically when an object is created)
    def __init__(self):
        # This line runs when the object is created
        print("I am a simple class.")

# Creating an object of the class
# obj = PythonSimple() calls the __init__() automatically
obj = PythonSimple()
