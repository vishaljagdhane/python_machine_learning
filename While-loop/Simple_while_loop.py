print("Why use while loop in python")
print("While loop is used to repeat a block of code as long as a certain condition is true.")
print("Syntax of while loop in python")

a = 1  # Initialize a variable
while a <= 10:  # Condition to check to continue the loop
    print("This is a simple while loop iteration:", a)  # Print statement
    a = a + 1  # Increment the value of a by 1 before next check

print("This is the end of while loop program")
# Note: Be cautious with while loops to avoid infinite loops by ensuring the condition will eventually become false.