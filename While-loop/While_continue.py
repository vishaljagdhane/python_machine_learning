print("While Loop with Continue Statement")
i=0 #This initializes the variable 
while i<=10: #This is the while loop condition
    i+=1 #This increments the variable by 1
     #This is the continue statement
    if i==5: # checks if i is equal to 5
        continue # skips the rest of the loop when i is 5
     #This prints the value of i in each iteration
    print("This is Iteration number withe Continue",i) #This prints the value of i in each iteration

