print("Simple and Basic String Data Type")
my_string = "Hello, World!"
print(my_string)
# String concatenation
greeting = "Hello"
name = "Alice"
full_greeting = greeting + ", " + name + "!"
print(full_greeting)
# String repetition
repeat_string = "Ha! " * 3
print(repeat_string)
# Caluate Total length of String
print("Length of the string is:", len(my_string))
# Accessing characters in a string
first_character = my_string[0]
print("First character:", first_character)

# Slicing a string
print("Sliced string:", my_string[0:5])  # Output: Hello