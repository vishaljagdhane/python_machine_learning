print("Print total array")

points = [1, 4, 2, 9, 7, 8, 9, 3, 1]
print("Array values:", points)

# Count how many times a specific value appears (e.g., count of 9)
count_nine = points.count(9)
print("Number of times 9 appears:", count_nine)

# Get total number of elements in the list using len()
total_elements = len(points)
print("Total number of elements in the array:", total_elements)
